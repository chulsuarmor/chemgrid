# Configuration
MCP_SERVER_URL = "http://127.0.0.1:8000"
PYTHON_EXECUTABLE = sys.executable
SERVER_SCRIPT = os.path.join("agents", "mcp_server", "server.py")
CHEMGRID_SCRIPT = os.path.join("agents", "10_testing_build", "integrated", "draw.py")
LOG_DIR = "training_logs"
FAIL_LOG = os.path.join(LOG_DIR, "failed_attempts.json")
SUCCESS_LOG = os.path.join(LOG_DIR, "success_patterns.json")
